package clases;

/**
 *
 * @author renzo
 */
public class User {
    private int idU;
    private String nombreU;
    private String apellidoU;
    private String cedulaU;
    private String correoU;
    private String usuarioU;
    private String contrasenaU;

    public int getIdU() {
        return idU;
    }

    public void setIdU(int idU) {
        this.idU = idU;
    }

    public String getNombreU() {
        return nombreU;
    }

    public void setNombreU(String nombreU) {
        this.nombreU = nombreU;
    }

    public String getApellidoU() {
        return apellidoU;
    }

    public void setApellidoU(String apellidoU) {
        this.apellidoU = apellidoU;
    }
    
    public String getCedulaU() {
        return cedulaU;
    }

    public void setCedulaU(String cedulaU) {
        this.cedulaU = cedulaU;
    }

    public String getCorreoU() {
        return correoU;
    }

    public void setCorreoU(String correoU) {
        this.correoU = correoU;
    }

    public String getUsuarioU() {
        return usuarioU;
    }

    public void setUsuarioU(String usuarioU) {
        this.usuarioU = usuarioU;
    }

    public String getContrasenaU() {
        return contrasenaU;
    }

    public void setContrasenaU(String contrasenaU) {
        this.contrasenaU = contrasenaU;
    }

    
    
}
